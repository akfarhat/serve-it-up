function getFeaturedItems(n) {
	var featuredItems = [];
	
	for (var i=0; i < n; i++) {
		var randomItem = menuItems[Math.floor(Math.random() * menuItems.length)];
		
		if(featuredItems.indexOf(randomItem) === -1) {
			featuredItems.push(randomItem);
		}
		else {
			i--;
		}
	}
	return featuredItems;
}

function getItem(itemName, menu) {
	var n = menu.length;
	for (var i=0; i < n; i++) {
		if (menu[i].name.toLowerCase() === itemName.toLowerCase()) {
			return menu[i];
		}
	}
	return null;
}

function getCategoryItems(categoryName, menu) {
	var n = menu.length;
	var items = [];
	
	for (var i=0; i < n; i++) {
		if (menu[i].category === categoryName) {
			items.push(menu[i]);
		}
	}
	
	return items;
}

function pathToImage(name) {
	return encodeURIComponent('imgs/' + name + '.jpg');
}

function getAllIngredients(menu) {
	var n = menu.length;
	var ingredients = [];
	
	for (var i=0; i < n; i++) {
		for (var j=0; j < menu[i].ingredients.length; j++) {
			var ingredient = menu[i].ingredients[j];
			if(ingredients.indexOf(ingredient) === -1) {
				ingredients.push(ingredient);
			}
		}
	}
	
	return ingredients;
}

function search (searchTerm) {
	var item = getItem(searchTerm, menuItems);
	if(item) return [item];
	
	var searchResults = [];
	
	for (var i=0; i < menuItems.length; i++) {
		if (menuItems[i].name.toLowerCase().indexOf(searchTerm.toLowerCase()) !== -1) {
			searchResults.push(menuItems[i]);
		}
		else if (menuItems[i].ingredients.indexOf(searchTerm.toLowerCase()) !== -1) {
			searchResults.push(menuItems[i]);
		}
	}
	
	return searchResults;
}

$('document').ready(function () {
	$.widget( "custom.catcomplete", $.ui.autocomplete, {
		_renderMenu: function( ul, items ) {
		  var that = this,
			currentCategory = "";
		  $.each( items, function( index, item ) {
			if ( item.category != currentCategory ) {
			  ul.append( "<li class='ui-autocomplete-category'>" + item.category + "</li>" );
			  currentCategory = item.category;
			}
			that._renderItemData( ul, item );
		  });
		}
	});
	
	var dishes = $.map(menuItems, function (menuItem) { return { label: menuItem.name, category: "Dishes"};});
	var allIngredients = getAllIngredients(menuItems);
	var ingredients = $.map(allIngredients, function (ingredient) { return { label: ingredient, category: "Ingredients"};});
	
	var data = dishes.concat(ingredients);
 
    $( "#searchInput" ).catcomplete({
		delay: 0,
		source: function(request, response) {
			var matches = $.map(data, function(dataItem) {
				var re = new RegExp("\\b" + request.term.toLowerCase());
				if (re.test(dataItem.label.toLowerCase())) {
					return dataItem;
				}
			});
			response(matches);
		}
    });
});